package com.go2it.edu.lecture2.datatypes;

public class ConcatenationByteAndStringAndLong {
    public static void main(String[] args) {
        byte b1 = 10;
        String statement = " this is the sum of numbers: ";
        Long l = 123400000L;
        System.out.println(b1 + statement + l);
    }
}
